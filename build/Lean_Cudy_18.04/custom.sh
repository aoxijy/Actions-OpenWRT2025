# 更新并安装源
echo "🔄 清理旧 feeds..."
./scripts/feeds clean
echo "🔄 更新所有 feeds..."
./scripts/feeds update -a
echo "📥 安装所有 feeds（强制覆盖冲突项）..."
./scripts/feeds install -a -f
echo "📥 再次安装所有 feeds（确保完整）..."
./scripts/feeds install -a -f
